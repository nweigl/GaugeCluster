DataDisplay::DataDisplay() {
}

void DataDisplay::setup() {
  Serial.begin(9600);

  // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if(!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("SSD1306 allocation failed"));
    for(;;); // Don't proceed, loop forever
  }

  drawBowtie();
}

void DataDisplay::drawOdometer(long miles) {
	if (miles > 9999999)
	{
		miles = 9999999;
	}
	else if (miles < 0)
	{
		miles = 0;
	}
	char buffer[7];

	sprintf(buffer, "%07ld", miles);
	display.clearDisplay();
	display.setFont(&FreeSerifBold12pt7b);
	display.setTextColor(SSD1306_WHITE);
	
	for (int i = 0; i < 6; i++) {	
		display.setCursor(18*i+3, 22);
		display.print(buffer[i]);
		display.drawLine(18 * i, 0, 18 * i, 32, SSD1306_WHITE);
	}

	display.setTextColor(SSD1306_BLACK); // Draw 'inverse' text
	display.setCursor(18*6+3, 22);
	display.fillRect(18*6+2, 0, 16, 32, SSD1306_WHITE);
	display.print(buffer[6]);
	display.drawLine(18 * 6, 0, 18 * 6, 32, SSD1306_WHITE);
	display.drawLine(127, 0, 127, 32, SSD1306_WHITE);
	display.display();
}

void DataDisplay::drawBowtie(void) {
  display.clearDisplay();

  display.drawBitmap(0, 0, bowtie, display.width(), display.height(), 1);
  display.display();
  delay(2000);
}
